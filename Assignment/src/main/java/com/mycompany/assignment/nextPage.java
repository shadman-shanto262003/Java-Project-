    private void plusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_plusActionPerformed
        // TODO add your handling code here:
        if((text1.getText().equals("")) || (text2.getText().equals(""))){
            missingtxt.setText("Please Provide Information!");
        }
        else{
             missingtxt.setText("");
            try{
                double a=Double.parseDouble(text1.getText());
                double b=Double.parseDouble(text2.getText());
                
                double r=a+b;
                result.setText("Anis,the addition is "+r);
            }
            catch(NumberFormatException e){
                result.setText("Number Format Missing!, "
                        + "Please Provide Valid Number!");
            }
        }
    }//GEN-LAST:event_plusActionPerformed

