    private void dividedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dividedActionPerformed
        // TODO add your handling code here:
        if((text1.getText().equals("")) || (text2.getText().equals(""))){
            missingtxt.setText("Please Provide Information!");
        }
        else{
            missingtxt.setText("");
            try{
                double a=Double.parseDouble(text1.getText());
                double b=Double.parseDouble(text2.getText());
                
                
                if(b==0){
                      result.setText("Anis,the division can't be determined due to an error!");
                }
                else{
                    double d=a/b;
                result.setText("Anis,the division is "+d);
                }
               
            }
            catch(NumberFormatException e){
                result.setText("Number Format Missing!, "
                        + "Please Provide Valid Number!");
            }
            
        }
    }//GEN-LAST:event_dividedActionPerformed

